#ifndef DOTPRODUCT_H
#define DOTPRODUCT_H

/**
* Name: dotProduct
* Process: Calculates the dot product of two integer arrays of the same size.
* Function input/parameters: arr1 - the first integer array
*                            arr2 - the second integer array
*                            size - the size of the arrays
* Function output/parameters: None
* Function output/returned: The dot product of the two arrays
* Dependencies: None
**/
int dotProduct(int arr1[], int arr2[], int size);

#endif /* DOTPRODUCT_H */
