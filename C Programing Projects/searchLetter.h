#ifndef SEARCHLETTER_H
#define SEARCHLETTER_H

/**
* Name: searchLetter
* Process: Searches for a specific value in a one-dimensional array of characters and returns its position (index).
* Function input/parameters: arr - the one-dimensional array of characters
*                            size - the size of the array
*                            letter - the value to search for
* Function output/parameters: None
* Function output/returned: The position (index) of the letter in the array, or -1 if not found
* Dependencies: None
**/
int searchLetter(char arr[], int size, char letter);

#endif /* SEARCHLETTER_H */
